#include "../inc/push_swap.h"

int	sort(t_env *g)
{
	t_pile	*tmp;
	int		i;

	i = 0;
	tmp = g->pile_b;
	while (tmp)
	{
		check_pos(g, tmp->nb);
		i++;
		tmp = tmp->next;
	}
	return (1);
}
